package lk.joinus.jobportal.dto;

public class EducationCenterDTO {

    private String userName;
    private String id;
    private String educationalCenterType;
    private String educationalCenterName;
    private String getEducationalCenterAddress;
    private String branch;
    private String telNo;
    private String faxNo;
    private String getEducationalCenterEmail;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEducationalCenterType() {
        return educationalCenterType;
    }

    public void setEducationalCenterType(String educationalCenterType) {
        this.educationalCenterType = educationalCenterType;
    }

    public String getEducationalCenterName() {
        return educationalCenterName;
    }

    public void setEducationalCenterName(String educationalCenterName) {
        this.educationalCenterName = educationalCenterName;
    }

    public String getGetEducationalCenterAddress() {
        return getEducationalCenterAddress;
    }

    public void setGetEducationalCenterAddress(String getEducationalCenterAddress) {
        this.getEducationalCenterAddress = getEducationalCenterAddress;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getTelNo() {
        return telNo;
    }

    public void setTelNo(String telNo) {
        this.telNo = telNo;
    }

    public String getFaxNo() {
        return faxNo;
    }

    public void setFaxNo(String faxNo) {
        this.faxNo = faxNo;
    }

    public String getGetEducationalCenterEmail() {
        return getEducationalCenterEmail;
    }

    public void setGetEducationalCenterEmail(String getEducationalCenterEmail) {
        this.getEducationalCenterEmail = getEducationalCenterEmail;
    }
}
